function calculprix() {
  var name = $('#choix-model option:selected').text();
  var part = $('#quantity').val();
  var prix = 0;
  switch (name) {
    case mariage1:
    prix = part * prixmariage1;
    break;

    case mariage2:
    prix = part * prixmariage2;
    break;


    case mariage3:
    prix = part * prixmariage3;
    break;

    case piecemontee1:
    prix = part * prixpiecemontee1;
    break;

    case piecemontee2:
    prix = part * prixpiecemontee2;
    break;

    case piecemontee3:
    prix = part * prixpiecemontee3;
    break;

    case bapteme1:
    prix = part * prixbapteme1;
    break;

    case bapteme2:
    prix = part * prixbapteme2;
    break;

    case bapteme3:
    prix = part * prixbapteme3;
    break;

    case annversaire1:
    prix = part * prixanniversaire1;
    break;

    case annversaire2:
    prix = part * prixanniversaire2;
    break;

    case annversaire3:
    prix = part * prixanniversaire3;
    break;

    case coktail1:
    prix = part * prixcoktail1;
    break;

    case coktail2:
    prix = part * prixcoktail2;
    break;

    case coktail3:
    prix = part * prixcoktail3;
    break;

    default:

  }

  return Math.round(prix);
}
